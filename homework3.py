"""
Определите, можно ли от шоколадки размером a × b долек отломить c долек, если разрешается сделать один разлом по прямой между дольками (то есть разломить шоколадку на два прямоугольника).

Выведите yes или no соответственно.
"""
def check_breakable(a, b, c):
    # Проверяем, является ли количество долек, которые нужно отломить,
    # больше либо равным общему количеству долек в шоколадке.
    if c >= a * b:
        return "no"
    
    # Проверяем, является ли количество долек, которые нужно отломить,
    # делителями хотя бы одной из сторон шоколадки.
    if c % a == 0 or c % b == 0:
        return "yes"
    
    # Проверяем, является ли сумма обоих сторон шоколадки
    # делителем количества долек, которые нужно отломить.
    if (a + b) % c == 0:
        return "yes"
    
    # Если ни одно из условий не выполняется, значит шоколадка не может быть отломлена на c долек.
    return "no"

# Пример использования
a = 4
b = 6
c = 10

result = check_breakable(a, b, c)
print(result)  # output: yes